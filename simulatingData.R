expandRange <- function(data, fact = 3){
  # Calculate the minimum value
  min_data <- min(data)
  
  # Scale around the minimum to ensure non-negativity
  # Note: This approach increases the range by a factor of 3 in relation to the minimum value
  adjusted_data <- (data - min_data) * fact + min_data
  
  # Now check if any value is below 0, and if so, adjust the entire vector up
  if (any(adjusted_data < 0)) {
    adjusted_data <- adjusted_data - min(adjusted_data)
  }
  
  return(round(adjusted_data, 0))
}

adjustValues <- function(data) {
  # Calculate the mean of the data
  mean_val <- mean(data)
  
  # Define a pivot value for adjusting. Using slightly below the mean encourages reducing the average.
  pivot <- mean_val * 0.9
  
  # Initialize the transformed data vector
  transformed_data <- numeric(length(data))
  
  for (i in 1:length(data)) {
    if (data[i] <= pivot) {
      # For values at or below the pivot, slightly increase them using a moderate factor
      transformed_data[i] <- data[i] * (1 + (pivot - data[i]) / (10 * pivot))
    } else {
      # For values above the pivot, apply a reduction based on how much they exceed the pivot
      transformed_data[i] <- pivot + (data[i] - pivot) / (1 + (data[i] - pivot) / pivot)
    }
  }
  
  # Apply a final adjustment to ensure the average is reduced
  final_adjustment_factor <- 0.9
  transformed_data <- transformed_data * final_adjustment_factor
  
  return(round(transformed_data, 0))
}

